-- =====================================================
-- DATABASE SETUP - OCCUPATIONAL SAFETY TESTING SYSTEM
-- For use in Belarus (Республика Беларусь)
-- Execute in Supabase SQL Editor
-- =====================================================

-- Drop existing tables
DROP TABLE IF EXISTS study_materials CASCADE;
DROP TABLE IF EXISTS user_test_results CASCADE;
DROP TABLE IF EXISTS questions CASCADE;
DROP TABLE IF EXISTS tests CASCADE;
DROP TABLE IF EXISTS users CASCADE;

-- 1. Users table
CREATE TABLE users (
    id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    email TEXT,
    full_name TEXT,
    role TEXT DEFAULT 'employee',
    department TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 2. Tests table
CREATE TABLE tests (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    title TEXT NOT NULL,
    description TEXT,
    category TEXT,
    passing_score INTEGER DEFAULT 70,
    time_limit INTEGER DEFAULT 0,
    is_published BOOLEAN DEFAULT true,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 3. Questions table
CREATE TABLE questions (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    test_id UUID REFERENCES tests(id) ON DELETE CASCADE,
    question_text TEXT NOT NULL,
    question_type TEXT DEFAULT 'single',
    options JSONB DEFAULT '[]',
    correct_answer JSONB,
    points INTEGER DEFAULT 1,
    order_index INTEGER DEFAULT 0
);

-- 4. User test results
CREATE TABLE user_test_results (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID REFERENCES users(id) ON DELETE CASCADE,
    test_id UUID REFERENCES tests(id) ON DELETE CASCADE,
    score INTEGER,
    passed BOOLEAN,
    answers JSONB DEFAULT '[]',
    completed_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 5. Study materials table
CREATE TABLE study_materials (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    test_id UUID REFERENCES tests(id) ON DELETE CASCADE,
    category TEXT,
    title TEXT NOT NULL,
    content TEXT NOT NULL,
    order_index INTEGER DEFAULT 0,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Enable RLS
ALTER TABLE users ENABLE ROW LEVEL SECURITY;
ALTER TABLE tests ENABLE ROW LEVEL SECURITY;
ALTER TABLE questions ENABLE ROW LEVEL SECURITY;
ALTER TABLE user_test_results ENABLE ROW LEVEL SECURITY;
ALTER TABLE study_materials ENABLE ROW LEVEL SECURITY;

-- RLS Policies
CREATE POLICY "Users read" ON users FOR SELECT USING (true);
CREATE POLICY "Users update" ON users FOR UPDATE USING (auth.uid() = id);
CREATE POLICY "Users insert" ON users FOR INSERT WITH CHECK (true);

CREATE POLICY "Tests read" ON tests FOR SELECT USING (is_published = true);

CREATE POLICY "Questions read" ON questions FOR SELECT USING (true);

CREATE POLICY "Results read" ON user_test_results FOR SELECT USING (auth.uid() = user_id);
CREATE POLICY "Results insert" ON user_test_results FOR INSERT WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Study materials read" ON study_materials FOR SELECT USING (true);

